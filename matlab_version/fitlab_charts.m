function fitlab_charts(script_dir, x, y, w, b, history)
%FITLAB_CHARTS 保存拟合直线图与 Loss 曲线图

% 统一使用浅色背景与深色文本配色。
obs_color = [0.16, 0.56, 0.85];   % 数据点
fit_color = [0.90, 0.22, 0.21];   % 拟合线
loss_color = [0.12, 0.53, 0.90];  % 损失曲线
grid_color = [0.84, 0.85, 0.87];
txt_color = [0.13, 0.13, 0.13];

% 图1：样本散点 + 最终拟合直线。
fig1 = figure('Color', 'w', 'Position', [120, 120, 920, 560]);
% 在样本区间生成更密集的 x，用于绘制平滑拟合直线。
x_line = linspace(min(x), max(x), 200)';
y_line = w * x_line + b;
h_fit = plot(x_line, y_line, '-', 'Color', fit_color, 'LineWidth', 3.0);
hold on;
% 先绘制拟合线，再绘制散点，减少线条对散点的遮挡。
h_obs = scatter(x, y, 30, 'filled', ...
    'MarkerFaceColor', obs_color, ...
    'MarkerEdgeColor', [0.08, 0.33, 0.52], ...
    'LineWidth', 0.45, ...
    'MarkerFaceAlpha', 1.0, ...
    'MarkerEdgeAlpha', 1.0);
xlabel('x', 'Color', txt_color);
ylabel('y', 'Color', txt_color);
title('50 次迭代后的线性拟合结果', 'FontWeight', 'normal', 'Color', txt_color);
lg1 = legend( ...
    [h_obs, h_fit], ...
    {'观测数据', sprintf('拟合直线: y = %.4fx + %.4f', w, b)}, ...
    'Location', 'northwest', ...
    'Box', 'on');
ax1 = gca;
set(ax1, ...
    'Color', [1 1 1], ...
    'LineWidth', 1.1, ...
    'FontName', 'Arial', ...
    'FontSize', 11, ...
    'Box', 'on', ...
    'TickDir', 'out', ...
    'XColor', txt_color, ...
    'YColor', txt_color);
set(lg1, ...
    'TextColor', txt_color, ...
    'Color', [1 1 1], ...
    'EdgeColor', [0.65 0.65 0.65]);
grid on;
ax1.GridAlpha = 0.55;
ax1.GridColor = grid_color;
ax1.Layer = 'top';
try
    exportgraphics(ax1, fullfile(script_dir, 'fit_line_after_50.png'), 'Resolution', 320);
catch
    saveas(fig1, fullfile(script_dir, 'fit_line_after_50.png'));
end
close(fig1);

% 图2：loss 收敛曲线，观察优化过程是否稳定下降。
fig2 = figure('Color', 'w', 'Position', [140, 140, 920, 560]);
iter = history(:, 1);
loss = history(:, 4);
plot(iter, loss, '-', ...
    'Color', loss_color, ...
    'LineWidth', 2.8);
hold on;
plot(iter(1:5:end), loss(1:5:end), 'o', ...
    'MarkerSize', 5.5, ...
    'MarkerFaceColor', [1 1 1], ...
    'MarkerEdgeColor', loss_color, ...
    'LineWidth', 1.1);
xlabel('迭代次数', 'Color', txt_color);
ylabel('损失值（MSE）', 'Color', txt_color);
title('Loss 随迭代次数变化曲线', 'FontWeight', 'normal', 'Color', txt_color);
ax2 = gca;
set(ax2, ...
    'Color', [1 1 1], ...
    'LineWidth', 1.1, ...
    'FontName', 'Arial', ...
    'FontSize', 11, ...
    'Box', 'on', ...
    'TickDir', 'out', ...
    'XColor', txt_color, ...
    'YColor', txt_color);
grid on;
ax2.GridAlpha = 0.55;
ax2.GridColor = grid_color;
ax2.Layer = 'top';
try
    exportgraphics(ax2, fullfile(script_dir, 'loss_curve_50.png'), 'Resolution', 320);
catch
    saveas(fig2, fullfile(script_dir, 'loss_curve_50.png'));
end
close(fig2);

end
